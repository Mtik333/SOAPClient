#
# TABLE STRUCTURE FOR: RSI_SCREENING
#

DROP TABLE IF EXISTS RSI_SCREENING;

CREATE TABLE RSI_SCREENING (
  ID int(11) NOT NULL,
  MOVIE_ID int(11) NOT NULL,
  AUDITORIUM_ID int(11) NOT NULL,
  SCREENING_START timestamp NOT NULL DEFAULT CURRENT_TIMESTAMP ON UPDATE CURRENT_TIMESTAMP,
  PRIMARY KEY (ID),
  KEY AUDITORIUM_ID (AUDITORIUM_ID),
  KEY MOVIE_ID (MOVIE_ID),
  CONSTRAINT RSI_SCREENING_ibfk_1 FOREIGN KEY (AUDITORIUM_ID) REFERENCES RSI_AUDITORIUM (ID),
  CONSTRAINT RSI_SCREENING_ibfk_2 FOREIGN KEY (MOVIE_ID) REFERENCES RSI_MOVIE (ID)
) ENGINE=InnoDB DEFAULT CHARSET=utf8 COLLATE=utf8_unicode_ci;

